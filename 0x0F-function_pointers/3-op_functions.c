#include "3-calc.h"

/**
 * op_add - calculates the + of two integers
 * @a: int 1.
 * @b: int 2.
 * Return: sum.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates the + of two integers
 * @a: int 1.
 * @b: int 2.
 * Return: difference.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the * of two integers
 * @a: int 1.
 * @b: int 2.
 * Return: product.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates the / of two integers
 * @a: int 1.
 * @b: int 2.
 * Return: result of the division.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the % of the division of two integers.
 * @a: int 1.
 * @b: int 2.
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
